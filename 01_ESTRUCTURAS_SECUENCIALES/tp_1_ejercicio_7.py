primerNum = int(input("Ingresa el primer numero entero distinto de 0: "));
segundoNum = int(input("Ingresa el segundo numero entero distinto de 0: "));

print(f"El resultado de sumarlos es: {primerNum+segundoNum}");
print(f"El resultado de dividirlos es: {primerNum/segundoNum}");
print(f"El resultado de multiplicarlos es: {primerNum*segundoNum}");
print(f"El resultado de restarlos es: {primerNum-segundoNum}");